const STYLE_TAIL =
  "Vogue editorial fashion photography, photorealistic, ultra detailed, professional studio lighting, magazine cover quality, premium luxury salon, soft warm color grading, no text, no watermark, no logo, no brand, no signature";

export function getLookImageUrl(rec: LookRecommendation, opts: { width?: number; height?: number } = {}): string {
  if (rec.localImage) return rec.localImage;
  const width = opts.width ?? 720;
  const height = opts.height ?? 960;
  const prompt = `${rec.name}, ${CATEGORY_IMAGE_HINT[rec.category]}. ${STYLE_TAIL}`;
  // Stable seed per look so the same picture comes back every time
  const seed = Math.abs(
    rec.id.split("").reduce((acc, c) => (acc * 31 + c.charCodeAt(0)) | 0, 0),
  );
  const encoded = encodeURIComponent(prompt);
  return `https://image.pollinations.ai/prompt/${encoded}?width=${width}&height=${height}&seed=${seed}&model=flux&nologo=true&enhance=true`;
}

